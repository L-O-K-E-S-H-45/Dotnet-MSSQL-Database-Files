CREATE TABLE EMPLOYEES (
	EMP_ID INT PRIMARY KEY,
	EMP_NAME VARCHAR(100) NOT NULL,
	JOB VARCHAR(10) NOT NULL,
	MGR_ID INT NOT NULL,
	HIREDATE DATE NOT NULL,
